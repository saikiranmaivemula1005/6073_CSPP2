public class Swap {
/**
*swap program.
*/
protected Swap() { }
/**
*swap program.
*/

    public static void main(String[] args) {
/**
@param a,b,temp
*/
        int a = 1, b = 2, temp;
        temp = a;
        a = b;
        b = temp;
        System.out.println(a);
        System.out.println(b);
    }
}
